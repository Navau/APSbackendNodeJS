//FUNCION PARA INSERTAR UN RENTA FIJA
async function Insertar(req, res) {
  const body = req.body;
  const { clave_instrumento, fecha_emision } = body;

  if (!clave_instrumento || !fecha_emision) {
    respDatosNoRecibidos400(
      res,
      "No se recibio clave_instrumento y fecha_emision"
    );
    return;
  }

  const queryClaveInstrumento = EscogerInternoUtil(nameTable, {
    select: ["*"],
    where: [
      {
        key: "clave_instrumento",
        value: clave_instrumento,
      },
      {
        key: "fecha_emision",
        value: fecha_emision,
      },
    ],
  });

  const claveInstrumentoValidacion = await pool
    .query(queryClaveInstrumento)
    .then((result) => {
      if (result.rowCount > 0) {
        return false;
      } else {
        return true;
      }
    })
    .catch((err) => {
      respErrorServidor500END(res, err);
      return null;
    });

  if (claveInstrumentoValidacion === false) {
    respResultadoIncorrectoObjeto200(
      res,
      null,
      result.rows,
      "Clave de Instrumento ya se encuentra registrada"
    );
  }
  if (claveInstrumentoValidacion === null) {
    return;
  }

  if (Object.entries(body).length === 0) {
    respDatosNoRecibidos400(res);
  } else {
    const params = {
      body: body,
    };
    let query = InsertarUtil(nameTable, params);
    pool.query(query, (err, result) => {
      if (err) {
        respErrorServidor500(res, err);
      } else {
        if (!result.rowCount || result.rowCount < 1) {
          respResultadoVacio404(res);
        } else {
          respResultadoCorrecto200(
            res,
            result,
            "Información guardada correctamente"
          );
        }
      }
    });
  }
}

//FUNCION PARA ACTUALIZAR UN RENTA FIJA
function Actualizar(req, res) {
  const body = req.body;

  let query = "";

  if (Object.entries(body).length === 0) {
    respDatosNoRecibidos400(res);
  } else {
    let idInfo = ValidarIDActualizarUtil(nameTable, body);
    if (!idInfo.idOk) {
      respIDNoRecibido400(res);
    } else {
      const params = {
        body: body,
        idKey: idInfo.idKey,
        idValue: idInfo.idValue,
      };
      query = ActualizarUtil(nameTable, params);

      pool.query(query, (err, result) => {
        if (err) {
          respErrorServidor500(res, err);
        } else {
          if (!result.rowCount || result.rowCount < 1) {
            respResultadoVacio404(res);
          } else {
            respResultadoCorrecto200(
              res,
              result,
              "Información actualizada correctamente"
            );
          }
        }
      });
    }
  }
}

